class Solution{
public:
    int maxInstance(string s){
        unordered_map<char,int> m;
        for(int i=0; i<s.length(); i++)
        m[s[i]]++;
        int ans = min(m['b'],min(m['a'],min(m['l']/2,min(m['o']/2,m['n']))));
        return ans;
    }
};

