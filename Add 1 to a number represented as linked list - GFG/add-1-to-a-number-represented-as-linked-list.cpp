class Solution
{
    public:
    Node* addOne(Node *head) 
    {
        // Your Code here
        // return head of list after adding one
        stack<Node*> st;
        Node* org = head;
        int rem = 1;
        while(head)
        {
            st.push(head);
            head = head->next;
        }
        while(!st.empty())
        {
            Node* temp = st.top();
            st.pop();
            temp->data += rem;
            rem = 0;
            if(temp->data>9)
            {
                rem = temp->data/10;
                temp->data %= 10;
            }
        }
        if(rem)
        {
            Node* n = new Node(rem);
            n->next = org;
            org = n;
        }
        return org;
    }
};

